/**
 * Build-time generator for the US state-boundary vector data used by the
 * Journey Map (FR-5: visible individual state boundaries).
 *
 * Fetches the us-atlas states TopoJSON (Census Bureau cartographic boundaries),
 * decodes the delta-encoded arcs, projects every vertex with the SAME
 * equirectangular projection the app uses (kept in sync with src/ui/geo.ts),
 * and emits a single TS module with:
 *   - STATE_LINES_PATH: one combined SVG path of every state boundary arc
 *     (drawn once each → crisp single-stroke state lines incl. coastline).
 *   - US_LAND_PATH: the filled lower-48 land mass (nation object).
 *
 * Re-run with:  node scripts/gen-us-states.mjs
 * The output is committed so the build/CI never needs network access.
 */
import { writeFileSync } from 'node:fs';

// ---- Projection (MUST match src/ui/geo.ts) --------------------------------
const MAP_W = 1000;
const MAP_H = 540;
const LON_MIN = -125;
const LON_SPAN = 59;
const LAT_MAX = 49.5;
const LAT_SPAN = 25;
const project = (lon, lat) => ({
  x: ((lon - LON_MIN) / LON_SPAN) * MAP_W,
  y: ((LAT_MAX - lat) / LAT_SPAN) * MAP_H,
});

const SRC = 'https://cdn.jsdelivr.net/npm/us-atlas@3/states-10m.json';

// Drop Alaska/Hawaii/territories — the route map is the lower-48 corridor.
// FIPS state ids: AK=02, HI=15, PR=72, etc. Keep only contiguous 48 + DC.
const EXCLUDE = new Set(['02', '15', '60', '66', '69', '72', '78']);

const topo = await (await fetch(SRC)).json();
const { scale, translate } = topo.transform;

/** Decode one TopoJSON arc index (handles negative = reversed). */
function decodeArc(idx) {
  const reversed = idx < 0;
  const arc = topo.arcs[reversed ? ~idx : idx];
  let x = 0;
  let y = 0;
  const pts = arc.map(([dx, dy]) => {
    x += dx;
    y += dy;
    return [x * scale[0] + translate[0], y * scale[1] + translate[1]]; // -> lon,lat
  });
  return reversed ? pts.slice().reverse() : pts;
}

/** Project a lon/lat ring and emit "x,y x,y ..." with light point-dropping. */
function ringToCoords(ring) {
  const out = [];
  let px = null;
  let py = null;
  for (const [lon, lat] of ring) {
    const { x, y } = project(lon, lat);
    const rx = Math.round(x * 10) / 10;
    const ry = Math.round(y * 10) / 10;
    if (px === null || Math.abs(rx - px) + Math.abs(ry - py) >= 0.4) {
      out.push(`${rx},${ry}`);
      px = rx;
      py = ry;
    }
  }
  return out;
}

// ---- State boundary lines: draw each arc once -----------------------------
const keptArcs = new Set();
for (const g of topo.objects.states.geometries) {
  if (EXCLUDE.has(g.id)) continue;
  const polys = g.type === 'Polygon' ? [g.arcs] : g.arcs;
  for (const poly of polys) for (const ring of poly) for (const idx of ring) keptArcs.add(idx < 0 ? ~idx : idx);
}
const lineSubpaths = [];
for (const arcIdx of keptArcs) {
  const coords = ringToCoords(decodeArc(arcIdx));
  if (coords.length < 2) continue;
  lineSubpaths.push('M' + coords.join('L'));
}
const STATE_LINES_PATH = lineSubpaths.join('');

// ---- Land fill: every lower-48 state polygon (excludes AK/HI/territories) -
// Built from the same filtered geometries as the lines so the fill never
// includes Alaska's antimeridian-crossing Aleutians (which blow up the bbox).
const fillSubpaths = [];
for (const g of topo.objects.states.geometries) {
  if (EXCLUDE.has(g.id)) continue;
  const polys = g.type === 'Polygon' ? [g.arcs] : g.arcs;
  for (const poly of polys) {
    for (const ring of poly) {
      // Stitch the ring's arcs into a continuous lon/lat loop.
      const loop = [];
      for (const idx of ring) for (const p of decodeArc(idx)) loop.push(p);
      const coords = ringToCoords(loop);
      if (coords.length < 3) continue;
      fillSubpaths.push('M' + coords.join('L') + 'Z');
    }
  }
}
const US_LAND_PATH = fillSubpaths.join('');

const out = `/**
 * AUTO-GENERATED by scripts/gen-us-states.mjs — do not edit by hand.
 *
 * US state-boundary vector data for the Journey Map (FR-5), pre-projected to
 * the app's ${MAP_W}x${MAP_H} equirectangular SVG space (see src/ui/geo.ts).
 * Source: us-atlas (US Census Bureau cartographic boundary files), lower-48.
 */

/** Every state boundary, each arc drawn once → crisp single-stroke state lines. */
export const STATE_LINES_PATH = ${JSON.stringify(STATE_LINES_PATH)};

/** Filled lower-48 land mass (for the base land gradient). */
export const US_LAND_PATH = ${JSON.stringify(US_LAND_PATH)};
`;

const target = new URL('../src/ui/usStates.ts', import.meta.url);
writeFileSync(target, out);
console.log(
  `Wrote ${target.pathname}: ${keptArcs.size} arcs, lines=${(STATE_LINES_PATH.length / 1024).toFixed(0)}KB, land=${(US_LAND_PATH.length / 1024).toFixed(0)}KB`,
);
